package com.WebApp;

public class Watch {
	private String brand;
	private int price;
	private String colour;
	private String shrapType;
	private int modelYear;
	
	
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getColour() {
		return colour;
	}
	public void setColour(String colour) {
		this.colour = colour;
	}
	public String getShrapType() {
		return shrapType;
	}
	public void setShrapType(String shrapType) {
		this.shrapType = shrapType;
	}
	public int getModelYear() {
		return modelYear;
	}
	public void setModelYear(int modelYear) {
		this.modelYear = modelYear;
	}
}
